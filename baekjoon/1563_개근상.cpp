#include <bits/stdc++.h>
using namespace std;
const int MOD = 1000000;
int N, d[1001][3][2]; // day, absent, late
int main() {
    scanf("%d", &N);
    d[1][0][0] = 1;
    d[1][0][1] = 1;
    d[1][1][0] = 1;
    for (int i=2;i<=N;++i) {
        d[i][0][0] = (d[i-1][0][0] + d[i-1][1][0] + d[i-1][2][0]) % MOD;
        d[i][1][0] = d[i-1][0][0];
        d[i][2][0] = d[i-1][1][0];
        d[i][0][1] = (d[i-1][0][0] + d[i-1][1][0] + d[i-1][2][0] + d[i-1][0][1] + d[i-1][1][1] + d[i-1][2][1]) % MOD;
        d[i][1][1] = d[i-1][0][1];
        d[i][2][1] = d[i-1][1][1];
    }
    printf("%d", (d[N][0][0] + d[N][1][0] + d[N][2][0] + d[N][0][1] + d[N][1][1] + d[N][2][1]) % MOD);
}